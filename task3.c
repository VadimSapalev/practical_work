#include <stdio.h>

#define MAX_R 30

// Функція для підрахунку кількості чисел із р розрядів, використовуючи цифри 5 та 9,
// де три однакові цифри не стоять поруч
unsigned long long countNumbers(int r) {
    // dp[i][j][k] - кількість чисел довжиною i, які закінчуються на j,
    // з k підрядкових однакових цифр
    unsigned long long dp[MAX_R+1][2][3] = {0};

    // Ініціалізація базових випадків
    dp[1][0][1] = 1; // число 5
    dp[1][1][1] = 1; // число 9

    // Заповнення dp масиву для довжини від 2 до r
    for (int i = 2; i <= r; i++) {
        // Кількість чисел довжиною i, які закінчуються на 5
        dp[i][0][1] = dp[i-1][1][1] + dp[i-1][1][2]; // попередня цифра 9
        dp[i][0][2] = dp[i-1][0][1]; // попередня цифра 5 з одним підрядковим

        // Кількість чисел довжиною i, які закінчуються на 9
        dp[i][1][1] = dp[i-1][0][1] + dp[i-1][0][2]; // попередня цифра 5
        dp[i][1][2] = dp[i-1][1][1]; // попередня цифра 9 з одним підрядковим
    }

    // Сума всіх допустимих чисел довжиною r
    return dp[r][0][1] + dp[r][0][2] + dp[r][1][1] + dp[r][1][2];
}

int main() {
    int r;
    printf("Введіть кількість розрядів (р ≤ 30): ");
    scanf("%d", &r);

    if (r <= 0 || r > 30) {
        printf("Неправильне значення р. Повинно бути 1 ≤ р ≤ 30.\n");
        return 1;
    }

    unsigned long long result = countNumbers(r);
    printf("Кількість чисел із %d розрядів: %llu\n", r, result);

    return 0;
}
